char* itoa_hex(unsigned int value, char* result, int digits) {
  char* ptr = result, *ptr1 = result, temp_char;
  unsigned int temp_value;

  do {
    temp_value = value;
    value /= 16;
    *ptr++ = "0123456789ABCDEF" [temp_value - value * 16];
    digits--;
  } while ( value );

  while (digits > 0) {
    *ptr++ = '0'; digits--;
  }

  *ptr-- = '\0';
  while(ptr1 < ptr) {
    temp_char = *ptr; *ptr--= *ptr1; *ptr1++ = temp_char;
  }
  return result;
}

